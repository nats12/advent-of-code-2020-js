const formattedInput = input.split("\n");

const slopes = [
    [1, 1],
    [3, 1],
    [5, 1],
    [7, 1],
    [1, 2]
];

const rowLength = formattedInput[0].length;
let totalTrees = 1;

function calculateTrees(x, y) {

    let currentXSpot = x;
    let currentYSpot = y; 

    let numberOfTrees = 0;

    while(currentYSpot < formattedInput.length) {
    
        let currentLocation = formattedInput[currentYSpot][currentXSpot];

        if(currentLocation === "#") {
            numberOfTrees++;
        }

        currentYSpot += y;
        currentXSpot = (currentXSpot + x) % rowLength; 
    }


    return numberOfTrees;
}

for(let i = 0; i < slopes.length; i++) {
    totalTrees = calculateTrees(slopes[i][0], slopes[i][1]) * totalTrees;
}


